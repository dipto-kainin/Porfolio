import { mkdir, readdir, writeFile } from 'node:fs/promises';
import path from 'node:path';
import { fileURLToPath } from 'node:url';

const __filename = fileURLToPath(import.meta.url);
const __dirname = path.dirname(__filename);
const rootDir = path.resolve(__dirname, '..');
const wallpapersDir = path.join(rootDir, 'public', 'wallpapers');
const outputFile = path.join(rootDir, 'src', 'data', 'wallpapers.ts');
const supportedExtensions = new Set(['.jpg', '.jpeg', '.png', '.webp', '.avif']);

const toSlug = (value) =>
  value
    .toLowerCase()
    .replace(/[^a-z0-9]+/g, '-')
    .replace(/^-+|-+$/g, '') || 'wallpaper';

const toLabel = (value) =>
  value
    .replace(/[_-]+/g, ' ')
    .replace(/\s+/g, ' ')
    .trim();

await mkdir(wallpapersDir, { recursive: true });

const entries = await readdir(wallpapersDir, { withFileTypes: true });
const files = entries
  .filter((entry) => entry.isFile())
  .map((entry) => entry.name)
  .filter((name) => supportedExtensions.has(path.extname(name).toLowerCase()))
  .sort((left, right) => left.localeCompare(right, undefined, { numeric: true }));

if (files.length === 0) {
  throw new Error(`No wallpaper images found in ${wallpapersDir}`);
}

const options = files.map((filename) => {
  const extension = path.extname(filename);
  const basename = path.basename(filename, extension);

  return {
    name: toSlug(basename),
    label: toLabel(basename),
    imageUrl: `/wallpapers/${encodeURIComponent(filename)}`,
  };
});

const fileContents = `import type { WallpaperOption } from '../types/desktop';

// This file is auto-generated by scripts/generate-wallpapers.mjs.
export const wallpaperOptions: WallpaperOption[] = ${JSON.stringify(options, null, 2)};

export const defaultWallpaper = wallpaperOptions[0];
`;

await writeFile(outputFile, fileContents);
